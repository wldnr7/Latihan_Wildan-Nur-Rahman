package com.matematik;

public class Matematika {


    void pertambahan (int a, int b){

        int hasil = a + b;

        System.out.printf("Pertambahan : %d + %d = %d\n",a,b,hasil);
    }
    void pengurangan (int a, int b){

        int hasil = a - b;

        System.out.printf("Pengurangan : %d - %d = %d\n",a,b,hasil);
    }
    void perkalian (int a, int b){

        int hasil = a * b;

        System.out.printf("Perkalian : %d * %d = %d\n",a,b,hasil);
    }
    void pembagian (int a, int b){

        int hasil = a / b;

        System.out.printf("Pembagian : %d / %d = %d\n",a,b,hasil);
    }

}
