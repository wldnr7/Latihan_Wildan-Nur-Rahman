package com.Matematika_03;

public class Matematika {

    //pertambahan
    protected void Pertambahan(int a, int b){

        int hasil = a + b;
        System.out.println("Hasil : " + hasil);
    }
    //uji konsep overloading
    protected void Pertambahan(double a, double b, double c){

        double hasil = a + b + c;
        System.out.println("Hasil : " + hasil);

    }
    protected void Pertambahan(double a, double b){

        double hasil = a + b;
        System.out.println("Hasil : " + hasil);
    }












}
